int main(int argc, char **argv)
{
    //initial pin tool
    if (PIN_Init(argc, argv))
    {
        return usage();
    }
    //initial pin symbols table
    PIN_InitSymbols();

    //rtn level
    RTN_AddInstrumentFunction(rtn, 0);

    //call when finish
    PIN_AddFiniFunction(finish, 0);

    //start pin program
    PIN_StartProgram();

    return 0;
}
